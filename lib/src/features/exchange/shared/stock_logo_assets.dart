String? _koreanStockLogoAssetPath(String stockCode) {
  final normalized = stockCode.trim();
  if (normalized.length != 6 || !_koreanStockLogoCodes.contains(normalized)) {
    return null;
  }
  return 'assets/stock_logos/kr/$normalized.png';
}

String? _usStockLogoAssetPath(String ticker) {
  final normalized = ticker.trim().toUpperCase();
  if (normalized.isEmpty || !_usStockLogoTickers.contains(normalized)) {
    return null;
  }
  return 'assets/stock_logos/us/$normalized.png';
}
